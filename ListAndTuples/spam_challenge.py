menu = [
    ["egg", "bacon"],
    ["egg", "sausage", "bacon"],
    ["egg", "spam"],
    ["egg", "bacon", "spam"],
    ["egg", "bacon", "sausage", "spam"],
    ["spam", "bacon", "sausage", "spam"],
    ["spam", "sausage", "spam", "bacon", "spam", "tomato", "spam"],
    ["spam", "egg", "spam", "spam", "bacon", "spam"],
]

for inner in menu:
    if "spam" in inner:
        upper_bound = len(inner) - 1 # get the upper index
        for index, item in enumerate(reversed(inner)):
            if item == "spam":
                del inner[upper_bound - index]
    print(inner)

